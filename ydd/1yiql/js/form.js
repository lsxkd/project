//得倒焦点的时候
	function judgeFocus(dom){		
		var id = dom.id;
		var value = dom.value;
		if( id == "user_name" && value == "请输入用户名"){	
			dom.value ='';
			dom.style.color = "#000";		
		}
		if( id == "user_pswd" && value == "请输入密码"){			
			dom.value = '';
			dom.type = 'password';
			dom.style.color = "#000";
		}
		if( id == "check_code" && value == "验证码"){			
			dom.value='';	
			dom.style.color = "#000";	
		}		
	}
	
	//失去焦点的时候
	function judgeBlur(dom){
		var id = dom.id;
		var value = dom.value;
		
		if( id == "user_name" && value == ""){		
			dom.value ='请输入用户名';
			dom.style.color = "#ccc";		
		}else{
			
			alert(isEmail(value));
			alert("请输入正确的邮箱账号")
			
		}
		if( id == "user_pswd" && value == ""){		
			dom.value = '请输入密码';
			dom.type = 'text';
			dom.style.color = "#ccc";
		}
		if( id == "check_code" && value == ""){			
			dom.value='验证码';	
			dom.style.color = "#ccc";	
		}	
	}
	
